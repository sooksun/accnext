        doc.fontSize(12)
           .font(baseFont)
           .text('‡∏£‡∏≤‡∏¢‡∏•‡∏∞‡πÄ‡∏≠‡∏µ‡∏¢‡∏î:');
        
        doc.fontSize(12)
           .font(baseFont)
           .text(transaction.description || '‡πÑ‡∏°‡πà‡∏£‡∏∞‡∏ö‡∏∏');
        
        doc.moveDown(0.5);

