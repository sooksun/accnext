        doc.moveDown(0.5);

        // Receipt details
        doc.fontSize(12)
           .font(baseFont)
           .text(`‡πÄ‡∏•‡∏Ç‡∏ó‡∏µ‡πà: ${transaction.id}`, { continued: true })
           .font(baseFont)
           .text(`  ‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà: ${new Date(transaction.date).toLocaleDateString('th-TH')}`);
        
        doc.moveDown(0.5);

